# Numeric Data Types Practice

# Integer (int)
print(type(123))        # <class 'int'>
print(type(0))          # <class 'int'>

# Float (floating-point number)
print(type(3.14))       # <class 'float'>
print(3.14 + 100)       # 103.14

# Arithmetic operations
print(199 - 1)          # 198
print(100 * 3)          # 300
print(100 / 10)         # 10.0

# Division-related
print(7 / 2)            # 3.5 → regular division (returns float)
print(7 // 2)           # 3   → floor division (returns integer)
print(100 // 10)        # 10
print(7 % 2)            # 1   → modulo (remainder)

# Exponentiation
print(2 ** 3)           # 8   → 2 to the power of 3